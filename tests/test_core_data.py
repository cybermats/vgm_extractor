def get_data():
    data = {
        "simple_100": bytearray(SIMPLE_100),
        "simple_100_none": bytearray(SIMPLE_100_NONE),
        "simple_101": bytearray(SIMPLE_101),
        "simple_110": bytearray(SIMPLE_110),
        "simple_150": bytearray(SIMPLE_150),
        "all_commands": bytearray(ALL_COMMANDS),
        "waits": bytearray(WAITS),
        "simple_ym2151": bytearray(SIMPLE_YM2151),
    }
    for k in data:
        size = len(data[k]) - 4
        (bsize) = struct.pack("<I", size)
        data[k][4:8] = bsize

    data["compressed"] = gzip.compress(data["simple_100"])

    return data
